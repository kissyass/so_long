#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*str;

	str = malloc(ft_strlen(s1) * sizeof(char) + 1);
	i = 0;
	if (!str)
		return (0);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
